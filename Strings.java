public class Strings {
    public static void main(String[] args) {

        // Assigning a string of text to a variable called 'text'
        String text = "This is some text!";

        System.out.println("\n\n" + text + "\n\n");

        // Adding text to an existing string variable
        text = text + " And this is more text!";

        System.out.println("\n\n" + text + "\n\n");

    }
    
}
